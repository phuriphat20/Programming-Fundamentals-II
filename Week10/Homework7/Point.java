public class Point 
{
    //Attribute
    private double x ;
    private double y ;

    //constructor
    public  Point(double x , double y)
    {
        this.x = x ;
        this.y = y ;
    }

    //Method
    public double getX() 
    {
        return x;
    }

    public double getY() 
    {
        return y;
    }
    
}